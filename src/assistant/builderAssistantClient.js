import {
  ASSISTANT_RESPONSE_VERSION,
  BUILDER_ASSISTANT_PAYLOAD_VERSION,
  createEmptyBuilderAssistantPayload,
} from "./assistantContracts.js";
import {
  validateAssistantResponse,
  validateBuilderAssistantPayload,
} from "./assistantValidation.js";
import { createPatchPreviewDiff } from "./diffHelpers.js";
